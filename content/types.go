package content

import (
	"io"

	"seehuhn.de/go/pdf"
)

// operator is a PDF operator found in a content stream.
type operator pdf.Name

// PDF implements the [pdf.Object] interface.
func (x operator) PDF(w io.Writer) error {
	_, err := w.Write([]byte(x))
	return err
}
